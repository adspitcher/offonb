package com.offonb.dataobjects;

public class BrandDataObject {
	
	private int id;
	private String name;
	public BrandDataObject(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}


}
